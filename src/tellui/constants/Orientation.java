package tellui.constants;

/**
 * Enumeration representing the two primary layout orientations for slider and
 * scroll components.
 */
public enum Orientation {
	/**
	 * Vertical orientation - components are arranged or operate in a top-to-bottom
	 * manner.
	 */
	VERTICAL,

	/**
	 * Horizontal orientation - components are arranged or operate in a
	 * left-to-right manner.
	 */
	HORIZONTAL;
}