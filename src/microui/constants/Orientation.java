package microui.constants;

/**
 * Enumeration representing the two primary layout orientations for UI components.
 * Used to specify the direction of layout, scrolling, or arrangement in various
 * UI elements such as sliders, scrollbars, containers, and layouts.
 */
public enum Orientation {
    /**
     * Vertical orientation - components are arranged or operate in a top-to-bottom manner.
     * Used for vertical scrollbars, vertical sliders, column layouts, etc.
     */
    VERTICAL,
    
    /**
     * Horizontal orientation - components are arranged or operate in a left-to-right manner.
     * Used for horizontal scrollbars, horizontal sliders, row layouts, etc.
     */
    HORIZONTAL;
}